package R2_ZmienneOperatoryInstrukcje;

// Operatory arytmetyczne

public class Zad_2_7 {
    public static void main(String[] args) {
        int a = 10, b = 3;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("Dodawanie " + (a + b));
        System.out.println("Odejmowanie " + (a - b));
        System.out.println("Mno≈ºenie " + (a * b));
        System.out.println("Dzielenie " + (a / b));
        System.out.println("Dzielenie modulo " + (a % b));

        System.out.println("Inkrementacja a " + ++a);
        System.out.println("Dekrementacja b " + --b);
    }
}
